#include "../libft.h"

/**
 * @brief Finds the last element of a linked list.
 * @param lst The t_list linked list.
 * @return A pointer to the last node in the list.
 */
t_list	*ft_lstlast(t_list *lst)
{
	if (lst == NULL)
		return (lst);
	while (lst->next != NULL)
		lst = lst->next;
	return (lst);
}

